#include<iostream>
using namespace std;
int main(){
 // declare variables p and q
 int p = 22, q = 10;
 int result;
 // addition operator
 result=p+q;
 cout<<(result)<<"\n";
 // subtraction operator
 cout<<(p - q)<<"\n"; 
 /*we can directly perform subtraction in print statement,no need to use result variable here*/
 // multiplication operator
 cout<<(p * q)<<"\n";
 // division operator
 cout<<(p / q)<<"\n";
 // modulo operator
 cout<<(p % q)<<"\n";
 }